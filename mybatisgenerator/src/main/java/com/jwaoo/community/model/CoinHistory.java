package com.jwaoo.community.model;

import java.util.Date;

public class CoinHistory {
    private Long id;

    private Long uid;

    private Integer reason;

    private Long reasonParam;

    private String reasonDetail;

    private Long coin;

    private Long fCoin;

    private Integer rebate;

    private Long objectId;

    private Byte osType;

    private Date createTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public Integer getReason() {
        return reason;
    }

    public void setReason(Integer reason) {
        this.reason = reason;
    }

    public Long getReasonParam() {
        return reasonParam;
    }

    public void setReasonParam(Long reasonParam) {
        this.reasonParam = reasonParam;
    }

    public String getReasonDetail() {
        return reasonDetail;
    }

    public void setReasonDetail(String reasonDetail) {
        this.reasonDetail = reasonDetail;
    }

    public Long getCoin() {
        return coin;
    }

    public void setCoin(Long coin) {
        this.coin = coin;
    }

    public Long getfCoin() {
        return fCoin;
    }

    public void setfCoin(Long fCoin) {
        this.fCoin = fCoin;
    }

    public Integer getRebate() {
        return rebate;
    }

    public void setRebate(Integer rebate) {
        this.rebate = rebate;
    }

    public Long getObjectId() {
        return objectId;
    }

    public void setObjectId(Long objectId) {
        this.objectId = objectId;
    }

    public Byte getOsType() {
        return osType;
    }

    public void setOsType(Byte osType) {
        this.osType = osType;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}